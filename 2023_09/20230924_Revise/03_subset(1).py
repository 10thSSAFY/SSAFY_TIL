# 부분 집합 생성 방법
# 비트 연산으로 부분 집합을 생성하는 법
N = 5
lst = [1, 2, 3, 4, 5]

for i in range(1, 1<<N):
    subset = []
    for j in range(N):
        if i & (1<<j):  # j번 비트가 0이 아니면
            subset.append(lst[j])
    print(subset)

