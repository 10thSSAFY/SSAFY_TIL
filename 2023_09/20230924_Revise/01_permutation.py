# 서로 다른 N개 원소 중 M개를 택하여 나열: 순열 (Permutation)
def Perm(k):
    if k == M:
        print(result)
        return

    for i in range(N):
        if not used[i]:
            result[k] = lst[i]
            used[i] = 1
            Perm(k+1)
            used[i] = 0


N, M = 5, 3
lst = [1, 2, 3, 4, 5]
used = [0] * N  # [0, 0, 0, 0, 0]
result = [0] * M  # [0, 0, 0]

Perm(0)

